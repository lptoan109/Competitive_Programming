#include <iostream>

using namespace std;

int main()
{
    double so1,so2;
    cin >> so1 >> so2;
    cout << so1 << " + " << so2 << " = " << so1+so2 << "\n";
    cout << so1 << " - " << so2 << " = " << so1-so2 << "\n";
    cout << so1 << " * " << so2 << " = " << so1*so2 << "\n";
    cout << so1 << " / " << so2 << " = " << so1/so2 << "\n";
    return 0;
}
